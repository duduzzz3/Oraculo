def sanear_acervo() -> int:
    alterados = 0
    agora = agora_brasilia_iso()
    with conectar() as conn:
        rows = conn.execute("SELECT * FROM obras").fetchall()
        for row in rows:
            novo_nome    = corrigir_nome_obra(row["nome_obra"], row["link_obra"])
            novo_autor   = corrigir_autor(row["autor"], novo_nome)
            tec_orig     = limpar_texto(row["tecnica_original"] or row["tecnica"])
            nova_tecnica = _tecnica_com_fallback_titulo(tec_orig, novo_nome)
            novo_ano     = normalizar_ano(row["ano_obra"])
            nova_dim     = padronizar_dimensoes(row["dimensoes"])
            if any([novo_nome  != row["nome_obra"],  novo_autor != row["autor"],
                    nova_tecnica != row["tecnica"],  novo_ano   != row["ano_obra"],
                    nova_dim   != row["dimensoes"]]):
                conn.execute(
                    "UPDATE obras SET nome_obra=?,autor=?,tecnica=?,tecnica_original=?,"
                    "ano_obra=?,dimensoes=?,atualizado_em=? WHERE id=?",
                    (novo_nome, novo_autor, nova_tecnica, tec_orig, novo_ano, nova_dim, agora, row["id"]),
                )
                alterados += 1
        conn.commit()
    if alterados:
        carregar_acervo.clear()
    return alterados

