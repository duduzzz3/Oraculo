def inserir_obras(df_db: pd.DataFrame) -> int:
    if df_db.empty:
        return 0
    inseridas = 0
    with conectar() as conn:
        for _, row in df_db.iterrows():
            dados = row.to_dict()
            try:
                conn.execute(
                    """INSERT INTO obras (nome_obra,autor,preco,dimensoes,tecnica,tecnica_original,
                        ano_obra,descricao,link_obra,link_imagem,site,origem,cotacao_dolar,coletado_em,atualizado_em)
                       VALUES (:nome_obra,:autor,:preco,:dimensoes,:tecnica,:tecnica_original,
                        :ano_obra,:descricao,:link_obra,:link_imagem,:site,:origem,:cotacao_dolar,:coletado_em,:atualizado_em)
                       ON CONFLICT(link_obra) DO UPDATE SET
                        nome_obra=excluded.nome_obra, autor=excluded.autor, preco=excluded.preco,
                        dimensoes=excluded.dimensoes, tecnica=excluded.tecnica,
                        tecnica_original=excluded.tecnica_original, ano_obra=excluded.ano_obra,
                        descricao=excluded.descricao, link_imagem=excluded.link_imagem,
                        site=excluded.site, origem=excluded.origem,
                        cotacao_dolar=excluded.cotacao_dolar, atualizado_em=excluded.atualizado_em""",
                    dados,
                )
                inseridas += 1
            except Exception:
                pass
        conn.commit()
    carregar_acervo.clear()
    return inseridas

