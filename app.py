def numero(v: Any) -> str:
    try:
        return f"{int(v):,}".replace(",", ".")
    except Exception:
        return "0"

def dinheiro(v: Any) -> str:
    try:
        if pd.isna(v):
            return SEM_INFO
        return f"R$ {float(v):,.2f}".replace(",", "X").replace(".", ",").replace("X", ".")
    except Exception:
        return SEM_INFO

def percentual(v: Any) -> str:
    try:
        return f"{float(v):.1f}%".replace(".", ",")
    except Exception:
        return SEM_INFO

def imagem_para_exibir(valor: Any) -> str | None:
    texto = limpar_texto(valor)
    if texto == SEM_INFO:
        return None
    if texto.startswith(("http://", "https://")):
        return texto
    caminho = Path(texto)
    if caminho.is_absolute() and caminho.exists():
        return str(caminho)
    relativo = BASE_DIR / texto
    if relativo.exists():
        return str(relativo)
    relativo2 = UPLOAD_DIR / texto
    if relativo2.exists():
        return str(relativo2)
    return None

# ─── Execução dos scrapers ────────────────────────────────────────────────────
def patch_legacy_script(codigo: str, max_obras: int, headless: bool) -> str:
    """Aplica patches nos scrapers legados (Blombo/Gagosian)."""
    limite = max_obras if max_obras > 0 else 999999
    codigo = re.sub(r"int\(os\.environ\.get\('MAX_OBRAS','\d+'\)\)\s*or\s*\d+", str(limite), codigo)
    codigo = re.sub(r"max_links\s*=\s*\d+", f"max_links = {limite}", codigo)
    h_str = "True" if headless else "False"
    codigo = re.sub(
        r"chromium\.launch\(headless\s*=\s*os\.environ\.get\([^)]+\)\s*==\s*['\"]true['\"\]]+\)",
        f"chromium.launch(headless={h_str})",
        codigo,
    )
    codigo = re.sub(r"chromium\.launch\(headless\s*=\s*(True|False)\)",
                    f"chromium.launch(headless={h_str})", codigo)
    return codigo

def executar_scraper(site_key: str, max_obras: int, headless: bool) -> tuple[bool, str, pd.DataFrame]:
    config   = SITES[site_key]
    runner   = config["runner"]
    blob_key = config["blob"]
    xlsx_name= config["xlsx"]

    workdir = COLETAS_DIR / f"{datetime.now().strftime('%Y%m%d_%H%M%S')}_{chave_coluna(site_key)}"
    workdir.mkdir(parents=True, exist_ok=True)

    # Decodifica o scraper embarcado
    codigo = _scraper_code(blob_key)

    if runner in {"saatchi_cli", "legacy_playwright", "playwright_cli"}:
        garantir_playwright_chromium()

    script_path = workdir / f"scraper_{blob_key.lower()}.py"

    if runner == "legacy_playwright":
        codigo = patch_legacy_script(codigo, max_obras, headless)
        script_path.write_text(codigo, encoding="utf-8")
        cmd = [sys.executable, str(script_path)]

    elif runner == "saatchi_cli":
        script_path.write_text(codigo, encoding="utf-8")
        limite = max_obras if max_obras > 0 else 999999
        cmd = [sys.executable, str(script_path),
               "--max", str(limite), "--paginas", "999",
               "--headless", "true" if headless else "false",
               "--saida", xlsx_name]

    elif runner == "artsoul_cli":
        script_path.write_text(codigo, encoding="utf-8")
        cmd = [sys.executable, str(script_path),
               "--max-obras", str(max_obras), "--max-pages", "0",
               "--output", xlsx_name]

    elif runner == "playwright_cli":
        script_path.write_text(codigo, encoding="utf-8")
        cmd = [sys.executable, str(script_path),
               "--max-obras", str(max_obras),
               "--output", xlsx_name,
               "--headless", "true" if headless else "false"]

    else:
        script_path.write_text(codigo, encoding="utf-8")
        cmd = [sys.executable, str(script_path)]

    env = os.environ.copy()
    env["PYTHONIOENCODING"] = "utf-8"
    env["MAX_OBRAS"]        = str(max_obras)
    env["HEADLESS"]         = "true" if headless else "false"

    try:
        proc = subprocess.run(
            cmd, cwd=workdir, env=env,
            capture_output=True, text=True, timeout=60 * 60,
        )
        log  = (proc.stdout or "") + "\n" + (proc.stderr or "")
        xlsx = workdir / xlsx_name
        if not xlsx.exists():
            return False, f"Arquivo de saída não encontrado.\n\n{log[-4000:]}", pd.DataFrame()

        header = int(config.get("header", 1))
        df = pd.read_excel(xlsx, header=header)
        # Auto-detect header se necessário
        if not any(str(c).strip().lower() in {"título", "titulo", "nome da obra", "nome_da_obra"}
                   for c in df.columns):
            try:
                df0 = pd.read_excel(xlsx, header=0)
                if any(str(c).strip().lower() in {"título", "titulo", "nome da obra", "nome_da_obra"}
                       for c in df0.columns):
                    df = df0
            except Exception:
                pass

        # Salva última coleta deste site
        df.to_excel(COLETAS_DIR / f"ultima_coleta_{chave_coluna(site_key)}.xlsx", index=False)
        return proc.returncode == 0, log[-5000:], df

    except subprocess.TimeoutExpired:
        return False, "Tempo máximo excedido durante a coleta.", pd.DataFrame()
    except Exception as exc:
        return False, f"Erro ao executar scraper: {exc}", pd.DataFrame()

