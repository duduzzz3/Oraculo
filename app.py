def aba_acervo(df: pd.DataFrame) -> None:
    st.header("Acervo")
    if df.empty:
        st.info("Nenhuma obra cadastrada ainda.")
        return

    c1, c2, c3, c4 = st.columns(4)
    sites    = c1.multiselect("Sites", sorted(df["site"].dropna().unique().tolist()), default=[])
    tecnicas = c2.multiselect("Técnicas", sorted(df["tecnica"].dropna().unique().tolist()), default=[])
    busca    = c3.text_input("Buscar por nome/autor")
    ordem    = c4.selectbox("Ordenar", ["Mais recentes", "Preço maior", "Preço menor", "Nome"])

    filtrado = df.copy()
    if sites:
        filtrado = filtrado[filtrado["site"].isin(sites)]
    if tecnicas:
        filtrado = filtrado[filtrado["tecnica"].isin(tecnicas)]
    if busca:
        b = busca.lower()
        filtrado = filtrado[filtrado.apply(
            lambda r: b in str(r.get("nome_obra", "")).lower()
                   or b in str(r.get("autor", "")).lower(), axis=1,
        )]
    if ordem == "Preço maior":
        filtrado = filtrado.sort_values("preco", ascending=False, na_position="last")
    elif ordem == "Preço menor":
        filtrado = filtrado.sort_values("preco", ascending=True,  na_position="last")
    elif ordem == "Nome":
        filtrado = filtrado.sort_values("nome_obra")

    st.caption(f"{len(filtrado)} obra(s) exibida(s)")
    cols = st.columns(3)
    for idx, (_, row) in enumerate(filtrado.iterrows()):
        with cols[idx % 3]:
            img = imagem_para_exibir(row.get("link_imagem"))
            if img:
                st.image(img, use_container_width=True)
            st.markdown(f"**{limpar_texto(row.get('nome_obra'))}**")
            st.caption(f"{limpar_texto(row.get('autor'))} · {limpar_texto(row.get('site'))}")
            st.write(f"**Preço:** {dinheiro(row.get('preco'))}")
            st.write(f"**Técnica:** {limpar_texto(row.get('tecnica'))}")
            st.write(f"**Dimensões:** {limpar_texto(row.get('dimensoes'))}")
            st.write(f"**Ano:** {limpar_texto(row.get('ano_obra'))}")
            link_obra = limpar_texto(row.get("link_obra"))
            if link_obra != SEM_INFO and link_obra.startswith(("http://", "https://")):
                st.link_button("Abrir obra no site", link_obra, use_container_width=True)


def aba_comparativo(df: pd.DataFrame) -> None:
    st.header("Comparativo")
    if df.empty or len(df) < 2:
        st.info("Cadastre ao menos duas obras para comparar.")
        return

    with st.expander("Metodologia de similaridade e níveis de valor justo", expanded=False):
        st.markdown(
            """
            O comparativo utiliza uma lógica de **abordagem de mercado**: a obra selecionada é
            confrontada com outras obras do acervo para verificar a força da evidência comparável.
            A pontuação total é de **0 a 100 pontos**.

            | Critério | Peso | Como é usado |
            |---|---:|---|
            | **Técnica**    | **28 pts** | Técnicas iguais aumentam fortemente a similaridade. |
            | **Dimensões**  | **25 pts** | Quanto mais próximas as dimensões, maior a pontuação. |
            | **Preço**      | **22 pts** | Compara a ordem de grandeza dos preços observados. |
            | **Ano**        | **15 pts** | Proximidade temporal da produção. |
            | **Autor**      | **10 pts** | Reforço quando a obra é do mesmo autor. |

            - **Nível 1:** similaridade ≥ 80. Evidência muito próxima.
            - **Nível 2:** similaridade 50–79. Evidência comparável com ajustes relevantes.
            - **Nível 3:** similaridade < 50. Baixa comparabilidade; exige maior julgamento técnico.
            """
        )

    nomes    = df.apply(lambda r: f"#{r['id']} · {r['nome_obra']} · {r['autor']}", axis=1).tolist()
    escolha  = st.selectbox("Obra de referência", nomes)
    id_ref   = int(re.match(r"#(\d+)", escolha).group(1))
    ref      = df[df["id"] == id_ref].iloc[0]

    st.subheader("Obra selecionada")
    cimg, cinfo = st.columns([1.1, 1.9], gap="large")
    with cimg:
        img = imagem_para_exibir(ref.get("link_imagem"))
        if img:
            st.image(img, use_container_width=True)
    with cinfo:
        st.markdown(f"## {limpar_texto(ref.get('nome_obra'))}")
        st.caption(f"{limpar_texto(ref.get('autor'))} · {limpar_texto(ref.get('site'))}")
        st.markdown(
            """<style>
            .oc-meta-grid{display:grid;grid-template-columns:repeat(4,minmax(100px,1fr));gap:12px;margin:10px 0 14px}
            .oc-meta-card{background:rgba(255,255,255,.02);border:1px solid rgba(255,255,255,.08);
              border-radius:12px;padding:12px 14px}
            .oc-meta-label{font-size:.86rem;opacity:.8;margin-bottom:4px}
            .oc-meta-value{font-size:.95rem;font-weight:600;line-height:1.25;word-break:break-word}
            </style>""",
            unsafe_allow_html=True,
        )
        st.markdown(
            f"""<div class="oc-meta-grid">
              <div class="oc-meta-card"><div class="oc-meta-label">Preço</div>
                <div class="oc-meta-value">{dinheiro(ref.get('preco'))}</div></div>
              <div class="oc-meta-card"><div class="oc-meta-label">Técnica</div>
                <div class="oc-meta-value">{limpar_texto(ref.get('tecnica'))}</div></div>
              <div class="oc-meta-card"><div class="oc-meta-label">Dimensões</div>
                <div class="oc-meta-value">{limpar_texto(ref.get('dimensoes'))}</div></div>
              <div class="oc-meta-card"><div class="oc-meta-label">Ano</div>
                <div class="oc-meta-value">{limpar_texto(ref.get('ano_obra'))}</div></div>
            </div>""",
            unsafe_allow_html=True,
        )
        if limpar_texto(ref.get("link_obra")) != SEM_INFO:
            st.link_button("Abrir obra original", str(ref.get("link_obra")))

    f1, f2, f3, f4 = st.columns(4)
    nivel_escolha = f1.multiselect("Níveis", ["Nível 1", "Nível 2", "Nível 3"],
                                   default=["Nível 1", "Nível 2", "Nível 3"])
    minimo      = f2.slider("Similaridade mínima", 0, 100, 0, 1)
    mesmo_site  = f3.checkbox("Somente mesmo site",  value=False)
    mesmo_autor = f4.checkbox("Somente mesmo autor", value=False)

    comps = []
    for _, row in df[df["id"] != id_ref].iterrows():
        score, pontos = similaridade(ref, row)
        nivel = classificar_nivel(score)
        if nivel not in nivel_escolha or score < minimo:
            continue
        if mesmo_site  and limpar_texto(row.get("site"))  != limpar_texto(ref.get("site")):
            continue
        if mesmo_autor and limpar_texto(row.get("autor")) != limpar_texto(ref.get("autor")):
            continue
        diag = diagnostico_comparacao(ref, row, pontos)
        comps.append({
            "Nível":           nivel,
            "Similaridade":    round(score, 2),
            "Nome da obra":    row["nome_obra"],
            "Autor":           row["autor"],
            "Site":            row["site"],
            "Preço":           dinheiro(row["preco"]),
            "Técnica":         row["tecnica"],
            "Dimensões":       row["dimensoes"],
            "Ano":             row["ano_obra"],
            "Pts Técnica":     round(pontos["Técnica"],    2),
            "Pts Dimensões":   round(pontos["Dimensões"],  2),
            "Pts Preço":       round(pontos["Preço"],      2),
            "Pts Ano":         round(pontos["Ano"],        2),
            "Pts Autor":       round(pontos["Autor"],      2),
            "_img":  row.get("link_imagem"),
            "_link": row.get("link_obra"),
            **diag,
        })

    if not comps:
        st.warning("Nenhuma obra similar encontrada com os filtros atuais.")
        return

    res = (pd.DataFrame(comps)
           .sort_values("Similaridade", ascending=False)
           .reset_index(drop=True))

    st.dataframe(
        res.drop(columns=["_img", "_link"]),
        use_container_width=True,
        hide_index=True,
        column_config={
            "Similaridade": st.column_config.ProgressColumn(
                "Similaridade", min_value=0, max_value=100, format="%.2f",
            ),
        },
    )

    st.subheader("Obras similares")
    st.markdown(
        """<style>
        .oc-sim-meta-grid{display:grid;grid-template-columns:repeat(4,minmax(100px,1fr));gap:12px;margin:14px 0}
        .oc-sim-pts-grid {display:grid;grid-template-columns:repeat(5,minmax(80px,1fr));gap:10px;margin:10px 0 14px}
        .oc-sim-card{background:rgba(255,255,255,.02);border:1px solid rgba(255,255,255,.08);
          border-radius:12px;padding:12px 14px}
        .oc-sim-label{font-size:.86rem;opacity:.8;margin-bottom:4px;font-weight:600}
        .oc-sim-value{font-size:.95rem;font-weight:600;line-height:1.25;word-break:break-word}
        </style>""",
        unsafe_allow_html=True,
    )
    for _, row in res.iterrows():
        a, b = st.columns([1.1, 1.9], gap="large")
        with a:
            img = imagem_para_exibir(row["_img"])
            if img:
                st.image(img, use_container_width=True)
        with b:
            st.markdown(f"## {limpar_texto(row['Nome da obra'])}")
            st.caption(f"{limpar_texto(row['Autor'])} · {limpar_texto(row['Site'])}")
            st.progress(
                min(max(float(row["Similaridade"]) / 100, 0.0), 1.0),
                text=f"{row['Nível']} · {row['Similaridade']:.2f}% de similaridade",
            )
            st.markdown(
                f"""<div class="oc-sim-meta-grid">
                  <div class="oc-sim-card"><div class="oc-sim-label">Preço</div>
                    <div class="oc-sim-value">{row['Preço']}</div></div>
                  <div class="oc-sim-card"><div class="oc-sim-label">Técnica</div>
                    <div class="oc-sim-value">{limpar_texto(row['Técnica'])}</div></div>
                  <div class="oc-sim-card"><div class="oc-sim-label">Dimensões</div>
                    <div class="oc-sim-value">{limpar_texto(row['Dimensões'])}</div></div>
                  <div class="oc-sim-card"><div class="oc-sim-label">Ano</div>
                    <div class="oc-sim-value">{limpar_texto(row['Ano'])}</div></div>
                </div>
                <div class="oc-sim-pts-grid">
                  <div class="oc-sim-card"><div class="oc-sim-label">Pts Técnica</div>
                    <div class="oc-sim-value">{row['Pts Técnica']}</div></div>
                  <div class="oc-sim-card"><div class="oc-sim-label">Pts Dimensões</div>
                    <div class="oc-sim-value">{row['Pts Dimensões']}</div></div>
                  <div class="oc-sim-card"><div class="oc-sim-label">Pts Preço</div>
                    <div class="oc-sim-value">{row['Pts Preço']}</div></div>
                  <div class="oc-sim-card"><div class="oc-sim-label">Pts Ano</div>
                    <div class="oc-sim-value">{row['Pts Ano']}</div></div>
                  <div class="oc-sim-card"><div class="oc-sim-label">Pts Autor</div>
                    <div class="oc-sim-value">{row['Pts Autor']}</div></div>
                </div>""",
                unsafe_allow_html=True,
            )
            if limpar_texto(row["_link"]) != SEM_INFO:
                st.link_button("Abrir obra similar", row["_link"],
                               key=f"link_{hash(str(row['_link']))}")
        st.divider()


